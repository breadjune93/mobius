/**
 * 랜덤 파일론 이미지 URL을 반환하는 함수
 * @returns {string} 랜덤 이미지 URL
 */
function getRandomPylonImage() {
    const randomIndex = Math.floor(Math.random() * PYLON_IMAGES.length);
    return PYLON_IMAGES[randomIndex];
}

/**
 * 특정 인덱스의 파일론 이미지 URL을 반환하는 함수
 * @param {number} index - 이미지 인덱스 (0-306)
 * @returns {string} 이미지 URL
 */
function getPylonImageByIndex(index) {
    if (index < 0 || index >= PYLON_IMAGES.length) {
        return getRandomPylonImage();
    }
    return PYLON_IMAGES[index];
}
